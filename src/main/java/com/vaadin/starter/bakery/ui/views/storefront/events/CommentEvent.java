package com.vaadin.starter.bakery.ui.views.storefront.events;

import com.vaadin.flow.component.ComponentEvent;
import com.vaadin.starter.bakery.ui.views.storefront.OrderDetailsFull;

public class CommentEvent extends ComponentEvent<OrderDetailsFull> {

	private Long orderId;
	private String message;

	public CommentEvent(OrderDetailsFull component, Long orderId, String message) {
		super(component, false);
		this.orderId = orderId;
		this.message = message;
	}

	public Long getOrderId() {
		return orderId;
	}

	public String getMessage() {
		return message;
	}
}